
package helloworld;

public class HelloWorld {

  
    public static void main(String[] args) {
        System.out.println("Hello,Word");
        /*
        ° "  public static  ", é um modificador de acesso do método;
        ° "  void  ", Tipo de retorno do método, ele não retorna nada, então por isso ele é void
        ° "  main  ", seria para executar o código que está dentro das chaves;
        ° "  out  ", vai sair para a tela, vamos conseguir ver o "Hello, Word";
        ° "  (String[] args)  ",  é Parâmetros/argumentos do Método
        --> O Java é Case Sensitive, ele se escrever algo com uma letra maior do que realmente deveria, ele não vai entender 
        
        ° "System.out.println('Hello Word')", é o código
        */
        
    }
}
