package variaveis;

/*O que são variáveis?
É Área de memória associada a um nome, que pode armazenar valores de um determinado tipo
Exemplo: armazenar idade de uma pessoa ou o nome de alguém

PARA CRIAR UMA VARIAVEL

<Tipo> <Nome da variável>;
<Tipo> <Nome da variável> = <valor>;
*/

public class Variaveis {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int idade = 20;
        String nome = "Lucas";
        String nomeDoMeuCachorro = "Adolf";
        
        System.out.println("A minha idade é "+idade);
        System.out.println("O meu nome é "+nome);
        
        
    }   
}
